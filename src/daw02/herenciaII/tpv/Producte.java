package herenciaII.tpv;

public class Producte {
    String nom;
    int preu;
    String marca;
    int fechaCaducidad;

    public Producte(String nom, int preu, String marca, int fechaCaducidad) {
        this.nom = nom;
        this.preu = preu;
        this.marca = marca;
        this.fechaCaducidad = fechaCaducidad;
    }
    
}
